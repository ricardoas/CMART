package client.clientMain;

public class ClientGeneratorException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 507790824195866359L;

	public ClientGeneratorException() {
		super();
	}

	public ClientGeneratorException(String message) {
		super(message);
	}

	public ClientGeneratorException(Throwable cause) {
		super(cause);
	}

	public ClientGeneratorException(String message, Throwable cause) {
		super(message, cause);
	}
}
